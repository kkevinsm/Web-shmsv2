    <script>
        function showLantai(lantai) {
            // Sembunyikan semua div lantai
            document.querySelector('.lantai-1').style.display = 'none';
            document.querySelector('.lantai-2').style.display = 'none';
            document.querySelector('.lantai-3').style.display = 'none';

            // Tampilkan div lantai yang dipilih
            document.querySelector('.lantai-' + lantai).style.display = 'flex';

            // Ubah status tombol menjadi aktif dan nonaktif
            const buttons = document.querySelectorAll('.btn-group .btn');
            buttons.forEach(button => button.classList.remove('btn-primary'));
            buttons.forEach(button => button.classList.add('btn-secondary'));

            // Set tombol yang dipilih menjadi aktif (primary)
            buttons[lantai - 1].classList.remove('btn-secondary');
            buttons[lantai - 1].classList.add('btn-primary');
        }
    </script>
